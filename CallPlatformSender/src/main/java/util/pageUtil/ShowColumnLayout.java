package util.pageUtil;

public abstract class ShowColumnLayout {

	private ShowColumn column;

	public void setShowColumn(ShowColumn column) {
		this.column = column;
	}

	public ShowColumn getShowColumn() {
		return this.column;
	}

	public abstract String[] getCssFiles();

	public abstract String getOutputHtml();

	public abstract String getOutputScript();
	
}
